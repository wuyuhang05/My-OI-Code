#define int LL
const int ha = 100003;
 
inline int qpow(int a,int n){
    int res = 1;
    while(n){
        if(n & 1) res = 1ll*res*a%ha;
        a = 1ll*a*a%ha;
        n >>= 1;
    }
    return res;
}
 
signed main(){
    LL m,n;scanf("%lld%lld",&m,&n);
    int ans = 1ll*m*qpow(m-1,n-1)%ha;
//  DEBUG(ans);
    ans = (qpow(m,n)+ha-ans)%ha;
    printf("%lld\n",ans);
    return 0;
}
