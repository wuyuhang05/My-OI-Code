const int ha = 10000+7;

inline int qpow(int a,int n=ha-2){
    int res = 1;
    while(n){
        if(n & 1) res = 1ll*res*a%ha;
        a = 1ll*a*a%ha;
        n >>= 1;
    }
    return res;
}

int T,n;

inline void Solve(){
    scanf("%d",&n);
    int res = (1ll*qpow(4,n)+1ll*qpow(2,n+1))%ha*qpow(4)%ha;
    printf("%d\n",res);
}

int main(){
    scanf("%d",&T);
    while(T--) Solve();
    return 0;
}